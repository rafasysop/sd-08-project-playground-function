function highestCount(array) {
  array.sort(function(a, b) {return a-b})
  let cont = 0;

  for (let i = array.length - 1; i > 0; i -= 1) {
    if (i == array.length - 1) {
      cont += 1;
    } else if (array[i] == array[i+1]) {
      cont += 1;
    } else {
      break;
    }
  }
  return cont;
}

